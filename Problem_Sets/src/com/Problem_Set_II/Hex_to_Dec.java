package com.Problem_Set_II;

public class Hex_to_Dec {
    public int Convert_H_to_D(String hex) {
        System.out.println("----------------------------");
        System.out.println("The decimal conversion of the given (hexadecimal) " + hex + " is:");
        int decimal = 0;
        try {
            decimal = Integer.parseInt(hex, 16); // Convert hexadecimal string to decimal
        } catch (NumberFormatException e) {
            System.out.println("Invalid hexadecimal number entered: " + hex);
        }
        return decimal;
    }
}
